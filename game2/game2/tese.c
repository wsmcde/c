#include"game.h"


void menu()
{
	printf("************************\n");
	printf("********1 .开始*********\n");
	printf("********0 .结束*********\n");
	printf("************************\n");
}

void game()
{
	char mine[ROWS][COLS] = { 0 };//存放布置的雷的信息
	char show[ROWS][COLS] = { 0 };//存放排查的雷的信息
	//初始化棋盘
	InitBoard(mine, ROWS, COLS,'0');//'0'
	InitBoard(show, ROWS, COLS,'*');//'*'
	//打印棋盘
	//DisPlayBoard(mine,ROW,COL);
	DisPlayBoard(show,ROW,COL);

	//设置雷
	SetMine(mine, ROW, COL);
	//DisPlayBoard(mine,ROW,COL);

	//排查雷
	FindMine(mine, show, ROW, COL);

}


int main()
{
	int input = 0;
	srand((unsigned int)time(NULL));
	do
	{
		menu();
		printf("请选择:>");
		scanf("%d", &input);
		switch (input)
		{
		case 1:
			game();
			break;
		case 0:
			printf("退出游戏\n");
			break;
		default:
			printf("选择错误重新选择\n");
			break;
		}
	} while (input);

	return 0;
}





